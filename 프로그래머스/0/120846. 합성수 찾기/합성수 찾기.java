class Solution {
    public int solution(int n) {
        int count;
        int result=0;
        for(int j=1;j<=n;j++){
            count=0;
            for(int i=1;i<=j;i++){
                if(j%i==0){
                    count++;
                
            }}
            if(count>=3){
                result+=1;
            }
       
                
            }
        return result;
        }
        
        }

